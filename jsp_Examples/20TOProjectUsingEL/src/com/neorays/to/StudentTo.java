package com.neorays.to;



public class StudentTo {
	
	private int id;
	private String name;
	private DepartmentTo dept;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public DepartmentTo getDept() {
		return dept;
	}
	public void setDept(DepartmentTo dept) {
		this.dept = dept;
	}

	
	
}
